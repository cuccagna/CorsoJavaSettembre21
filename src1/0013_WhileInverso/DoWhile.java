public class DoWhile {
    public static void main(String[] args) {
        System.out.println("\n\nPrimo esempio\n\n");
        //Stampa di 10 numeri
        int contatore = 1;

        do{
            System.out.printf("%d\n",contatore);
            contatore += 1;
        }while(contatore<=10);
    }
}
